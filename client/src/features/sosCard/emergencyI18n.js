const EMERGENCY_STRINGS = {
  en, de, fr, it, es, ckb, kmr, fa, ar, tr, pl, ru, uk, el, ur,
};

/**
 * Returns the emergency strings for the preferred code (falling back to the site language, then
 * English), always merged over English per key so a missing string can never crash the page.
 */
export function getEmergencyStrings(preferredCode, fallbackCode) {
  const pick = EMERGENCY_STRINGS[preferredCode] || EMERGENCY_STRINGS[fallbackCode] || en;
  return {
    ...en,
    ...pick,
    severities: { ...en.severities, ...(pick.severities || {}) },
    biologicalSexValues: { ...en.biologicalSexValues, ...(pick.biologicalSexValues || {}) },
    pregnancyValues: { ...en.pregnancyValues, ...(pick.pregnancyValues || {}) },
  };
}

