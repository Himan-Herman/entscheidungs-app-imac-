function setByPath(root, path, value) {
  const parts = path.split(".");
  let cur = root;
  for (let i = 0; i < parts.length - 1; i++) cur = cur[parts[i]];
  cur[parts[parts.length - 1]] = value;
}

/** Apply translateEs to every leaf (arrays included) */
function deepTranslateFull(obj) {
  if (typeof obj === "string") return translateEs(obj);
  if (Array.isArray(obj)) {
    return obj.map((x) => deepTranslateFull(x));
  }
  if (obj && typeof obj === "object") {
    const o = {};
    for (const [k, v] of Object.entries(obj)) o[k] = deepTranslateFull(v);
    return o;
  }
  return obj;
}

